/**
 * Generates wordpress-plugin/includes/class-command-defs.php
 * from shared/commands.ts.
 *
 * Run: npm run generate:php-defs
 */

import { writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));

const { COMMAND_SLUGS, TERMINAL_STATUSES, VALID_TRANSITIONS } =
	await import('../shared/commands.ts');

function phpArray(arr, indent = '') {
	const items = arr.map((v) => `${indent}\t'${v}'`).join(',\n');
	return `[\n${items},\n${indent}]`;
}

function phpAssocArray(obj, indent = '') {
	const entries = Object.entries(obj)
		.map(([key, values]) => {
			const valArr = phpArray(values, `${indent}\t`);
			return `${indent}\t'${key}' => ${valArr}`;
		})
		.join(',\n');
	return `[\n${entries},\n${indent}]`;
}

const output = `<?php
/**
 * Command definitions \u2014 auto-generated from shared/commands.ts.
 *
 * Do not edit this file directly. Run: npm run generate:php-defs
 *
 * @generated
 */

if ( ! defined( 'ABSPATH' ) ) {
	exit;
}

/**
 * Shared command definitions for the WordPress plugin.
 */
class Command_Defs {

	/**
	 * Allowed prompt values for command creation.
	 *
	 * @var string[]
	 */
	const ALLOWED_PROMPTS = ${phpArray(COMMAND_SLUGS, '\t')};

	/**
	 * Valid status transitions: current_status => [ allowed_next_statuses ].
	 *
	 * @var array<string, string[]>
	 */
	const VALID_TRANSITIONS = ${phpAssocArray(VALID_TRANSITIONS, '\t')};

	/**
	 * Terminal command statuses.
	 *
	 * @var string[]
	 */
	const TERMINAL_STATUSES = ${phpArray([...TERMINAL_STATUSES], '\t')};
}
`;

const outPath = resolve(
	__dirname,
	'../wordpress-plugin/includes/class-command-defs.php'
);
writeFileSync(outPath, output, 'utf8');

console.log(`Generated ${outPath}`);
